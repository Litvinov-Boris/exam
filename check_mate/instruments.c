#include "check_mate.h"

int ft_strlen(char *str)
{
    int i;

    i = 0;
    while (str[i] != '\0')
    {
        i++;
    }
    return (i);
}

int prep(char c)
{
    if (c == 'P' || c == 'R' || c == 'B' || c == 'Q')
        return (1);
    return (0);
}